def dedupe_preserve_order(values: List[str]) -> List[str]:
    seen = set()
    ordered: List[str] = []
    for value in values:
        if value not in seen:
            seen.add(value)
            ordered.append(value)
    return ordered


def normalize_language(language: str) -> str:
    key = language.strip().lower()
    return LANGUAGE_ALIASES.get(key, key)


def get_refusal_sequences(language: str) -> List[str]:
    normalized = normalize_language(language)
    native_sequences = LANGUAGE_SPECIFIC_REFUSAL_SEQUENCES.get(normalized, [])
    return dedupe_preserve_order(ENGLISH_REFUSAL_SEQUENCES + native_sequences)
